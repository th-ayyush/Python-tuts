#typecasting
#the coversion of one data type into another data type is known as type casting
#explicit type=user telling python to convert
a="1"
b="2"
print(a+b)#it will concatenate the a and b
#to solve this
print(int(a)+int(b))
#implicit type=python converts data type itself
c=1.9#higher priority data type will be used
d=8
print(c+d)

